unsigned int WaitACK(void)
{
	unsigned int data=0;
	
	data=(unsigned char)USART2_GetChar();
	if(data==ACK){return 0;}
	if(data==NACK){return 1;}
	if(data==0xAAAA){return 2;} //timeout error
	return data;
}


